package com.alpha.innohacksproject.Model;

public class NumberModel {

    String name;
    String number;

    public NumberModel() {
    }

    public NumberModel(String name, String number) {
        this.name = name;
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public String getNumber() {
        return number;
    }
}
